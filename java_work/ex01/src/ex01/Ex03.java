package ex01;

public class Ex03 {

	public static void main(String[] args) {
		System.out.println("10+3=" + (10 + 3));
		System.out.println(50 / 3); // 나누기 몫은 /
		System.out.println(50 % 3); // 나누기 나머지는 %
		System.out.println(3 * 4);
		System.out.println("10-7=" + (10 - 7));
	}

}

