package aa;

public class Ex04 {
	
	public static void main(String[] args) {
		
		// char 함수 : '' 대신 "" 사용 시, 한 문자 이상 사용할 시 에러
		String aa= "aquamera";
		char a = 'A';
		char b = 'B';
		
		System.out.println((int)a);
		System.out.println((int)b);
		// int : 숫자로 변환
		// A는 숫자 65, B는 숫자 66, C는 숫자 67로 지정
		// C언어: 실행속도 빠름, 대신 작성해야 하는 소스도 많음
		System.out.println(aa);
		
	}

}
