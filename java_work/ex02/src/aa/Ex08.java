public class Ex08 {
	
	// 결과값에 쌍따옴표 표시 => 맨 처음과 끝에 \" 집어넣기
	public static void main(String[] args) {
		
		System.out.println("\"안녕하세요\"\n\"권도희입니다.\"\n\t\"개발공부는.....\"");
	}
//	\b : 백스페이스 문자
//	\t : 탭 문자
//	\\ : 백슬래시 문자
//	\' : 작은 따옴표 문자
//	\" : 큰 따옴표 문자
//	\n : 다음 줄로 변환(개 행 문자)
//	\r : 캐리지 리턴 (carriage return)
}
